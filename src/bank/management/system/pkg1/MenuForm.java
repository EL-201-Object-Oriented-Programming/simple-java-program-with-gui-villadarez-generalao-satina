    private void SignUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpButtonActionPerformed
        // TODO add your handling code here:
        
        if ("".equals(Email.getText()) || "".equals(Password.getText()) || "".equals(ConfirmPassword.getText()) 
    || "".equals(FirstName.getText()) || "".equals(LastName.getText()) || "".equals(PIN.getText()) 
    || "".equals(ConfirmPIN.getText()) || "".equals(ContactNumber.getText()) || "".equals(Address.getText()) 
    || BirthDate.getDate() == null) {
    JOptionPane.showMessageDialog(new JFrame(), "All fields are required");
} else {
    String email = Email.getText();
    String password = Password.getText();
    String confirmPassword = ConfirmPassword.getText(); // Get Confirm Password
    String firstname = FirstName.getText();
    String lastname = LastName.getText();
    String pin = PIN.getText();          // Get the PIN
    String confirmPin = ConfirmPIN.getText(); // Get Confirm PIN
    String contactnum = ContactNumber.getText();  // Get the contact number
    String address = Address.getText();
    
    // **Email Validation**
    if (!email.matches("^[\\w._%+-]+@[\\w.-]+\\.com$")) {
        JOptionPane.showMessageDialog(new JFrame(), "Invalid email format. Email must contain '@' and end with '.com'.");
        return; // Stop further execution if the email is invalid
    }
    
    // Check if Email Already Exists
        String emailCheckQuery = "SELECT * FROM accountdetails WHERE Email = ?";
        try (PreparedStatement psCheck = con.prepareStatement(emailCheckQuery)) {
            psCheck.setString(1, email);
            try (ResultSet rs = psCheck.executeQuery()) {
                if (rs.next()) {
                    JOptionPane.showMessageDialog(new JFrame(), "Email is already registered. Please use a different email.");
                    return; // Stop further execution
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(new JFrame(), "Error checking email: " + ex.getMessage());
            return;
        }

    // Validate that PIN is exactly 4 digits
    if (!pin.matches("\\d{4}")) { // Regex for 4-digit numeric PIN
        JOptionPane.showMessageDialog(new JFrame(), "PIN must be exactly 4 digits.");
        return; // Stop further execution if PIN is invalid
    }

    // Check if Password and Confirm Password match
    if (!password.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(new JFrame(), "Password and Confirm Password must match.");
        return; // Stop further execution if passwords do not match
    }

    // Check if PIN and Confirm PIN match
    if (!pin.equals(confirmPin)) {
        JOptionPane.showMessageDialog(new JFrame(), "PIN and Confirm PIN must match.");
        return; // Stop further execution if PINs do not match
    }

    // Validate Contact Number: Must start with 09 and be exactly 11 digits
    if (!contactnum.matches("^09\\d{9}$")) { // Regex for "09" followed by 9 digits
        JOptionPane.showMessageDialog(new JFrame(), "Contact number must start with '09' and be exactly 11 digits.");
        return; // Stop further execution if contact number is invalid
    }

    // Retrieve the selected date from JDateChooser and convert it to java.sql.Date
    Date selectedDate = BirthDate.getDate();
    java.sql.Date birthdate = null;
    if (selectedDate != null) {
        birthdate = new java.sql.Date(selectedDate.getTime()); // Convert java.util.Date to java.sql.Date

        // Calculate the age based on the selected birthdate
        LocalDate birthLocalDate = selectedDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate currentDate = LocalDate.now();
        int age = Period.between(birthLocalDate, currentDate).getYears();

        // Check if age is below 18
        if (age < 18) {
            JOptionPane.showMessageDialog(new JFrame(), "You must be at least 18 years old to register.");
            return; // Stop further execution
        }
    }

    // Adjust the column names to match your table structure
    String querySignUp = "INSERT INTO accountdetails (Email, Password, Firstname, Lastname, Contactnum, Birthdate, Address, Balance, PIN) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
    try (PreparedStatement ps = con.prepareStatement(querySignUp)) {
        ps.setString(1, email);      // Set email
        ps.setString(2, password);   // Set password
        ps.setString(3, firstname);  // Set firstname
        ps.setString(4, lastname);   // Set lastname
        ps.setString(5, contactnum); // Set contact number
        if (birthdate != null) {
            ps.setDate(6, birthdate); // Set birthdate as java.sql.Date
        } else {
            // Handle the case where birthdate is null (if necessary)
            ps.setNull(6, java.sql.Types.DATE);
        }
        ps.setString(7, address);    // Set address
        ps.setDouble(8, 0.0);        // Set Balance to 0.0 initially
        ps.setString(9, pin);        // Set PIN

        ps.executeUpdate(); // Execute the query
        JOptionPane.showMessageDialog(new JFrame(), "Registered Successfully");
        
        // Clear all input fields
        Email.setText("");
        Password.setText("");
        ConfirmPassword.setText("");
        FirstName.setText("");
        LastName.setText("");
        PIN.setText("");
        ConfirmPIN.setText("");
        ContactNumber.setText("");
        Address.setText("");
        BirthDate.setDate(null); // Reset the JDateChooser field
        
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(new JFrame(), "Registration failed: " + ex.getMessage());
    }
}
        
        
    }//GEN-LAST:event_SignUpButtonActionPerformed

