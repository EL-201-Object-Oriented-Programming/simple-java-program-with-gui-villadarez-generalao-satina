    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          String email = LogEmail.getText();
    String password = LogPassword.getText();

    if ("".equals(email) || "".equals(password)) {
        JOptionPane.showMessageDialog(this, "Email and Password are required!");
    } else {
        // Check if the entered email and password exist in the database
        String queryLogin = "SELECT * FROM accountdetails WHERE Email = ? AND Password = ?";
        
        try (PreparedStatement ps = con.prepareStatement(queryLogin)) {
            // Set the parameters for the prepared statement
            ps.setString(1, email);  // Set email parameter
            ps.setString(2, password);  // Set password parameter

            // Execute the query
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {  // If there is a matching row
                    // Successfully logged in, proceed to LoggedPage JFrame
                    JOptionPane.showMessageDialog(this, "Login successful!");
                    
                    LoggedPage loggedPage = new LoggedPage(email);
                    loggedPage.setVisible(true);
                    this.dispose(); // Close the login form
                    String name = rs.getString("Firstname");
                    String balance = rs.getString("Balance");
                    LoggedPage.lbl_Name.setText(name);
                    LoggedPage.lbl_Balance.setText(balance);
                    LoggedPage.lbl_DBalance.setText(balance);
                    LoggedPage.lbl_WBalance.setText(balance);
                } else {
                    // Incorrect email or password
                    JOptionPane.showMessageDialog(this, "Invalid Email or Password!");
                    
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error executing query: " + ex.getMessage());
                ex.printStackTrace();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database connection error: " + ex.getMessage());
            ex.printStackTrace();
        }
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

